public class Tencent6vcsIssue {

    private String Issue;
    private String OnlineIssue;
    private String OnlineNumber;
    private String OnlineNumberMAX;
    private String TimeStamp;

    public String getIssue() {
        return Issue;
    }

    public void setIssue(String issue) {
        Issue = issue;
    }

    public String getOnlineIssue() {
        return OnlineIssue;
    }

    public void setOnlineIssue(String onlineIssue) {
        OnlineIssue = onlineIssue;
    }

    public String getOnlineNumber() {
        return OnlineNumber;
    }

    public void setOnlineNumber(String onlineNumber) {
        OnlineNumber = onlineNumber;
    }

    public String getOnlineNumberMAX() {
        return OnlineNumberMAX;
    }

    public void setOnlineNumberMAX(String onlineNumberMAX) {
        OnlineNumberMAX = onlineNumberMAX;
    }

    public String getTimeStamp() {
        return TimeStamp;
    }

    public void setTimeStamp(String timeStamp) {
        TimeStamp = timeStamp;
    }

}
