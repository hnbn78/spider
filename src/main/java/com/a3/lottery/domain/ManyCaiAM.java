package com.a3.lottery.domain;

public class ManyCaiAM {

    private String issue;
    private String opendate;
    private String code;
    private String lotterycode;
    private String officialissue;

    public String getIssue() {
        return issue;
    }

    public void setIssue(String issue) {
        this.issue = issue;
    }

    public String getOpendate() {
        return opendate;
    }

    public void setOpendate(String opendate) {
        this.opendate = opendate;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getLotterycode() {
        return lotterycode;
    }

    public void setLotterycode(String lotterycode) {
        this.lotterycode = lotterycode;
    }

    public String getOfficialissue() {
        return officialissue;
    }

    public void setOfficialissue(String officialissue) {
        this.officialissue = officialissue;
    }

}
